package com.sritt.parkingsnap;

public class Vehicle {
    String licence;
    String year;
    String make;
    String model;
    String color;

    public Vehicle(String licence, String year, String make, String model, String color) {
        this.licence = licence;
        this.year = year;
        this.make = make;
        this.model= model;
        this.color = color;
    }
}
